# 247 모듈 임포트
# 모듈을 임포트하는 4가지 방식에 대해 설명해보세요.

# 1. a.py 모듈을 import 
  # import a.py
  # a.py에 있는 b method 호출
  # a.b()
  # a.py에 있는 c method 호출
  # a.c()

# 2. a.py 모듈을 e 라는 별칭으로 정한다
  # import a.py as e
  # a.py의 별칭인 e를 통해서 b method 호출
  # e.b()
  # a.py의 별칭인 e를 통해서 c method 호출
  # e.c()

# 3. a.py 라는 모듈에서 b,c 라는 method를 가져온다
  # from a.py import b,c 
  # b method 호출
  # b()
  # c method 호출
  #c()

# 4. a.py 라는 모듈에 있는 모든 method를 가져온다.
  # from a.py import * 
  # b method 호출
  # b()
  # c method 호출
  # c()


